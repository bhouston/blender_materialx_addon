# Export operator
class MATERIALX_OT_export_material(Operator):
    """Export selected material to MaterialX format."""
    
    bl_idname = "materialx.export_material"
    bl_label = "Export MaterialX"
    bl_description = "Export the active material to MaterialX format"
    
    filepath: StringProperty(
        name="File Path",
        description="Path to save the MaterialX file",
        subtype='FILE_PATH'
    )
    
    export_textures: BoolProperty(
        name="Export Textures",
        description="Export texture files along with MaterialX",
        default=True
    )
    
    copy_textures: BoolProperty(
        name="Copy Textures",
        description="Copy texture files to export directory",
        default=False
    )
    
    relative_paths: BoolProperty(
        name="Relative Paths",
        description="Use relative paths for texture references",
        default=True
    )
    
    optimize_document: BoolProperty(
        name="Optimize Document",
        description="Optimize the MaterialX document",
        default=True
    )
    
    advanced_validation: BoolProperty(
        name="Advanced Validation",
        description="Perform advanced validation on exported documents",
        default=True
    )
    
    materialx_version: EnumProperty(
        name="MaterialX Version",
        description="MaterialX version to use",
        items=SUPPORTED_MATERIALX_VERSIONS,
        default=DEFAULT_MATERIALX_VERSION
    )
    
    def invoke(self, context, event):
        """Show file dialog when operator is invoked."""
        if not self.filepath:
            # Set default filepath
            material = context.active_object.active_material if context.active_object else None
            if material:
                default_name = f"{material.name}.mtlx"
            else:
                default_name = "material.mtlx"
            
            # Use addon preferences default path if available
            prefs = context.preferences.addons.get(__name__)
            if prefs and prefs.preferences.default_output_path:
                self.filepath = os.path.join(prefs.preferences.default_output_path, default_name)
            else:
                self.filepath = default_name
        
        context.window_manager.fileselect_add(self)
        return {'RUNNING_MODAL'}
    
    def execute(self, context):
        """Execute the export operation."""
        try:
            # Get the active material
            material = context.active_object.active_material if context.active_object else None
            if not material:
                self.report({'ERROR'}, "No active material found")
                return {'CANCELLED'}
            
            # Prepare export options
            options = {
                'export_textures': self.export_textures,
                'copy_textures': self.copy_textures,
                'relative_paths': self.relative_paths,
                'optimize_document': self.optimize_document,
                'advanced_validation': self.advanced_validation,
                'materialx_version': self.materialx_version
            }
            
            # Create exporter and export
            exporter = MaterialExporter(logger)
            success = exporter.export(context.active_object, self.filepath, options)
            
            if success:
                self.report({'INFO'}, f"Successfully exported {material.name} to {self.filepath}")
                return {'FINISHED'}
            else:
                self.report({'ERROR'}, f"Failed to export {material.name}")
                return {'CANCELLED'}
                
        except Exception as e:
            logger.error(f"Export failed: {e}")
            self.report({'ERROR'}, f"Export failed: {str(e)}")
            return {'CANCELLED'}

